package com.mygdx.game.client.view.spritesheet;

/**
 * represents the frame metadata from the aseprite generated data
 * @author elimonent
 *
 */
public class FrameMetadata {
	/**
	 * x position of bottom left corner of the frame
	 */
	protected int x;
	/**
	 * y position of the bottom left corner of the frame
	 */
	protected int y;
	/**
	 * width of the frame
	 */
	protected int w;
	/**
	 * height of the frame
	 */
	protected int h;
}
